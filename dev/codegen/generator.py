def _gen_path(area: str, filename: str) -> Path:
    """Resolve a flat target inside the STAGING tree `dev/codegen/generated/<area>/`.
    `generate` writes here and never touches the library; the separate `install` step
    promotes these files flat into `src/pylzt/<area>/` behind the ruff+mypy gate. The
    emitted files carry the module's final flat import paths (`pylzt.<area>.<name>`)."""
    target = STAGING_ROOT / area / filename
    target.parent.mkdir(parents=True, exist_ok=True)
    return target


_GEN_HEAD = (
    '"""Generated by forge — DO NOT EDIT.\n'
    "Sourced from the official readme.io OpenAPI spec; re-run `python -m dev.codegen build`\n"
    "to update.\n"
    '"""'
)


def _strip_docs(body: str) -> str:
    """Drop docstrings and comments so import detection scans code, not prose (an enum
    named Email must not be imported just because a docstring says "email")."""
    body = re.sub(r'"""(?:.|\n)*?"""', "", body)
    return re.sub(r"#[^\n]*", "", body)


def _imports_used(code: str, names: list[str], module: str) -> str | None:
    used = sorted({n for n in names if re.search(rf"\b{re.escape(n)}\b", code)})
    return f"from {module} import {', '.join(used)}" if used else None


def _lib_enum_module(body: str) -> str:
    head = f"{_GEN_HEAD}\n\nfrom __future__ import annotations\n\nfrom enum import StrEnum\n\n\n"
    return f"{head}{body}\n"


def _lib_model_file(
    api: str,
    model: ExtractedModel,
    backend: ModelBackend,
    reused: list[str],
    new_enums: list[str],
    model_names: list[str],
) -> str:
    """One model per file: `models/<api>/<snake>.py`. Sibling models referenced in this
    model's field annotations are real imports (the model graph is a DAG — nested-then-
    parent, deduped, cycles degraded to dict — so transitive imports resolve). Import order
    is normalised by ruff --fix during install, so grouping here is loose."""
    body = render_model(model, backend)
    code = _strip_docs(body)
    imports = ["from __future__ import annotations", ""]
    if re.search(r"\bAny\b", code):
        imports.append("from typing import Any")
    if backend is ModelBackend.DATACLASS:
        imports.append("from dataclasses import dataclass")
        if re.search(r"\bMapping\b", code):
            imports.append("from collections.abc import Mapping")
    else:
        base_imports = [n for n in ("LolzObject", "BaseResponse") if n in code]
        if base_imports:
            imports.append(f"from pylzt.models.base import {', '.join(base_imports)}")
        pyd = [n for n in ("Field", "AliasPath") if f"{n}(" in code]
        if pyd:
            imports.append(f"from pydantic import {', '.join(pyd)}")
    for sib in sorted(
        n for n in model_names if n != model.name and re.search(rf"\b{re.escape(n)}\b", code)
    ):
        imports.append(f"from pylzt.models.{api}.{_snake(sib)} import {sib}")
    for imp in (
        _imports_used(code, reused, "pylzt.types"),
        _imports_used(code, new_enums, f"pylzt.enums.{api}"),
    ):
        if imp:
            imports.append(imp)
    return f"{_GEN_HEAD}\n\n" + "\n".join(imports) + f"\n\n\n{body}"


# The response roots that shipped UNPREFIXED in the published library, per API. An explicit list,
# not a pattern: a rule like "alias every prefixed Response" also aliases models this generator has
# only just invented, which no caller can be importing — and for `StatusMessageResponse` that would
# hand the same bare name to two namespaces, the very ambiguity the prefix exists to prevent. This
# list is the compatibility surface and nothing else; it only ever shrinks, and empties at the next
# major.
_SHIPPED_UNPREFIXED: Final[dict[str, tuple[str, ...]]] = {
    "market": (
        "EmailLettersResponse",
        "ItemCodeDataResponse",
        "QueryDataAppIdResponse",
        "StatusItemResponse",
    ),
    "forum": ("DataDataTotalLinksResponse",),
}


def _compat_aliases(api: str, ordered: list[ExtractedModel]) -> list[tuple[str, str]]:
    """`(old_name, current_name)` for every response root this generator API-qualifies.

    The qualification is deliberate (see the API-prefix comment in `_normalize_models`): a
    field-derived name like `StatusItemResponse` is API-agnostic by construction, so market and
    forum both produce it and a downstream registry keyed by bare `__name__` collides. But the
    published library shipped the UNPREFIXED names, so regenerating renames five public types out
    from under anyone importing them.

    Emitting the old name as an alias keeps both: the registry gets globally unique class names,
    and existing imports keep resolving. Emitted from the generator rather than hand-written into
    the package, because `models/<api>/__init__.py` is generated — a hand-added alias there would
    be erased by the next run, which is exactly when it is still needed.
    """
    cap = _pascal(api)
    taken = {m.name for m in ordered}
    aliases = []
    for bare in _SHIPPED_UNPREFIXED.get(api, ()):
        qualified = f"{cap}{bare}"
        # Never shadow a model that genuinely owns the bare name, and never alias a name this run
        # did not actually produce.
        if qualified in taken and bare not in taken:
            aliases.append((bare, qualified))
    return aliases


def _model_init(api: str, models: list[ExtractedModel]) -> str:
    """The `models/<api>/__init__.py` that re-exports every generated model so callers keep
    importing `from pylzt.models.<api> import X`."""
    ordered = sorted(models, key=lambda m: m.name)
    aliases = _compat_aliases(api, ordered)
    lines = [_GEN_HEAD, ""]
    lines += [f"from pylzt.models.{api}.{_snake(m.name)} import {m.name}" for m in ordered]
    if aliases:
        lines += [
            "",
            "# Backward-compatible aliases: these names shipped unprefixed before the response",
            "# roots were API-qualified. Deprecated — prefer the qualified name; removable in the",
            "# next major, once no caller imports the bare form.",
        ]
        lines += [f"{old} = {new}" for old, new in aliases]
    lines += ["", "__all__ = ["]
    lines += [f'    "{name}",' for name in sorted([m.name for m in ordered] + [a for a, _ in aliases])]
    lines += ["]"]
    return "\n".join(lines) + "\n"


def _lib_method_module(
    api: str, body: str, model_names: list[str], reused: list[str], new_enums: list[str]
) -> str:
    code = _strip_docs(body)
    imports = ["from __future__ import annotations"]
    if re.search(r"\bAny\b", code):
        imports.append("from typing import Any")
    base_names = ["BaseMethod"]
    if re.search(r"\bpassthrough\b", code):
        base_names.append("passthrough")
    imports += ["", f"from pylzt.methods.base import {', '.join(base_names)}"]
    if "def parse_response" in code:
        imports.append("from pylzt.transport.base import Response")
    locals_ = [
        imp
        for imp in (
            _imports_used(code, ["Media"], "pylzt.media"),
            _imports_used(code, model_names, f"pylzt.models.{api}"),
            # HttpMethod/ApiTarget/RateClass live in pylzt.types (methods.base doesn't
            # re-export them under strict) — ApiTarget/RateClass only referenced by forum
            # method bodies (__api__/__rate_class__ overrides), _imports_used drops unused ones.
            _imports_used(
                code, ["HttpMethod", "ApiTarget", "RateClass", *reused], "pylzt.types"
            ),
            _imports_used(code, new_enums, f"pylzt.enums.{api}"),
        )
        if imp
    ]
    if locals_:
        imports += ["", *locals_]
    return f"{_GEN_HEAD}\n\n" + "\n".join(imports) + f"\n\n\n{body}"


def _facade_name(op: ExtractedOperation, taken: set[str]) -> str:
    base = _snake(op.operation_id.replace(".", "_")) or "call"
    name, i = base, 2
    while name in taken:
        name, i = f"{base}_{i}", i + 1
    taken.add(name)
    return name


# The one name added to every generated signature. Checked against all three specs: it collides
# with none of the 629 distinct parameter names they declare, while `cookies` (2 endpoints) and
# `extra` (3) are already taken — which is why the overrides travel as ONE bundle rather than as
# separate `headers=`/`cookies=`/`timeout=` arguments.
_OPTIONS_PARAM: Final = "request_options"


def render_facade_method(
    op: ExtractedOperation, class_name: str, root_name: str | None, facade_name: str
) -> str:
    params = _sorted_params(op)
    sig = ["self"]
    for p in params:
        ann = p.annotation if p.required else f"{p.annotation} | None"
        sig.append(f"{p.name}: {ann}" + ("" if p.required else " = None"))
    ret = root_name or "Any"
    args = ", ".join(f"{p.name}={p.name}" for p in params)
    # Keyword-only and last, so no existing positional call shifts.
    sig += ["*", f"{_OPTIONS_PARAM}: RequestOptions | None = None"]
    return (
        f"    async def {facade_name}({', '.join(sig)}) -> {ret}:\n"
        f"        {_format_docstring(op, indent='        ')}\n"
        f"        return await self({class_name}({args}), "
        f"{_OPTIONS_PARAM}={_OPTIONS_PARAM})"
    )


def render_sync_facade_method(
    op: ExtractedOperation, root_name: str | None, facade_name: str
) -> str:
    """Sync counterpart of `render_facade_method` — same signature, a blocking `def`
    body running the already-rendered async facade method through `SyncRunner`
    instead of re-parsing the spec (reuses `_sorted_params`/`_format_docstring`, the
    only two spec-derived shapes a call site needs)."""
    params = _sorted_params(op)
    sig = ["self"]
    for p in params:
        ann = p.annotation if p.required else f"{p.annotation} | None"
        sig.append(f"{p.name}: {ann}" + ("" if p.required else " = None"))
    ret = root_name or "Any"
    args = ", ".join(f"{p.name}={p.name}" for p in params)
    sig += ["*", f"{_OPTIONS_PARAM}: RequestOptions | None = None"]
    forwarded = f"{_OPTIONS_PARAM}={_OPTIONS_PARAM}"
    call_args = f"{args}, {forwarded}" if args else forwarded
    return (
        f"    def {facade_name}({', '.join(sig)}) -> {ret}:\n"
        f"        {_format_docstring(op, indent='        ')}\n"
        f"        return self._runner.run(self._async.{facade_name}({call_args}))"
    )


def _render_sync_facade_module(
    api: str,
    body: str,
    model_names: list[str],
    reused: list[str],
    new_enums: list[str],
) -> str:
    """Parallel sync facade — a thin blocking wrapper around `Generated{Cap}Facade`,
    not a second spec-derived implementation (no separate `__call__`/method-as-class
    path; every method just runs the async one through a `SyncRunner`)."""
    cap = api.capitalize()
    code = _strip_docs(body)
    imports = [
        "from __future__ import annotations",
        "",
        "from typing import TYPE_CHECKING, Any",
        "",
        f"from pylzt.facades.{api} import Generated{cap}Facade",
    ]
    for imp in (
        _imports_used(code, ["Media"], "pylzt.media"),
        _imports_used(code, model_names, f"pylzt.models.{api}"),
        _imports_used(code, reused, "pylzt.types"),
        _imports_used(code, new_enums, f"pylzt.enums.{api}"),
    ):
        if imp:
            imports.append(imp)
    imports.append("from pylzt.transport.base import RequestOptions")
    imports += ["", "if TYPE_CHECKING:", "    from pylzt.sync.runner import SyncRunner"]
    cls = [
        f"class SyncGenerated{cap}Facade:",
        f'    """Generated by forge — DO NOT EDIT. Blocking facade for the {api} API — '
        f"each method runs `Generated{cap}Facade`'s async counterpart through a "
        '`SyncRunner`, no separate spec-derived implementation."""',
        "",
        f"    def __init__(self, async_facade: Generated{cap}Facade, runner: SyncRunner) -> None:",
        "        self._async = async_facade",
        "        self._runner = runner",
        "",
        body,
    ]
    return f"{_GEN_HEAD}\n\n" + "\n".join(imports) + "\n\n\n" + "\n".join(cls) + "\n"


def _render_facade_module(
    api: str,
    body: str,
    method_imports: list[str],
    model_names: list[str],
    reused: list[str],
    new_enums: list[str],
) -> str:
    cap = api.capitalize()
    code = _strip_docs(body)
    imports = ["from __future__ import annotations", "", "from typing import TYPE_CHECKING, Any"]
    imports += ["", *sorted(set(method_imports))]
    for imp in (
        _imports_used(code, ["Media"], "pylzt.media"),
        _imports_used(code, model_names, f"pylzt.models.{api}"),
        _imports_used(code, reused, "pylzt.types"),
        _imports_used(code, new_enums, f"pylzt.enums.{api}"),
    ):
        if imp:
            imports.append(imp)
    # A real import, not a TYPE_CHECKING one: every generated signature carries it as a runtime
    # default annotation, and the module is `from __future__ import annotations` so it would still
    # type-check — but a caller doing `from pylzt.facades.market import RequestOptions` (the
    # obvious place to look for it) must find something.
    imports.append("from pylzt.transport.base import RequestOptions")
    imports += ["", "if TYPE_CHECKING:", "    from pylzt.methods.base import BaseMethod"]
    cls = [
        f"class Generated{cap}Facade:",
        f'    """Generated by forge — DO NOT EDIT. Async facade for the {api} API."""',
        "",
        "    if TYPE_CHECKING:",
        # Must mirror _Namespace.__call__ exactly: this stub is the only thing a type checker sees
        # for the delegation every generated method body performs, and the real __call__ lives on
        # the namespace that mixes this class in.
        "        async def __call__[T](",
        "            self, method: BaseMethod[T], *, "
        f"{_OPTIONS_PARAM}: RequestOptions | None = None",
        "        ) -> T: ...",
        "",
        body,
    ]
    return f"{_GEN_HEAD}\n\n" + "\n".join(imports) + "\n\n\n" + "\n".join(cls) + "\n"


def generate_all(spec: dict[str, Any], api: str, backend: ModelBackend) -> None:
    operations = extract_operations(spec)
    existing = _existing_endpoints()
    kept = [op for op in operations if (op.http_method, _normalize_path(op.path)) not in existing]

    op_root, op_unwrap, models, enums = _build_api_models(spec, kept)
    if backend is ModelBackend.PYDANTIC:
        op_root, models = _collapse_models(op_root, models, api)
        models = _rebase_status_responses(models)
    model_names = [m.name for m in models]
    reused = sorted({e.name for e in enums if e.name in EXISTING_TYPES_ENUMS})
    new_enums = [e for e in enums if e.name not in EXISTING_TYPES_ENUMS]
    new_enum_names = [e.name for e in new_enums]

    if new_enums:
        enum_body = "\n\n".join(render_enum(e) for e in new_enums)
        _gen_path("enums", f"{api}.py").write_text(_lib_enum_module(enum_body), encoding="utf-8")

    for m in models:
        _gen_path("models", f"{api}/{_snake(m.name)}.py").write_text(
            _lib_model_file(api, m, backend, reused, new_enum_names, model_names), encoding="utf-8"
        )
    _gen_path("models", f"{api}/__init__.py").write_text(_model_init(api, models), encoding="utf-8")

    by_domain: dict[str, list[ExtractedOperation]] = {}
    for op in kept:
        by_domain.setdefault(op.domain, []).append(op)

    facade_methods: list[str] = []
    sync_facade_methods: list[str] = []
    facade_imports: list[str] = []
    taken: set[str] = _client_method_names()  # never shadow a hand-written Client method
    for domain, ops in sorted(by_domain.items()):
        ops = sorted(ops, key=lambda o: o.operation_id)
        body = "\n\n".join(
            render_method_skeleton(
                op, op_root[op.operation_id], model_names, op_unwrap.get(op.operation_id), api
            )
            for op in ops
        )
        _gen_path("methods", f"{api}_{domain}.py").write_text(
            _lib_method_module(api, body, model_names, reused, new_enum_names), encoding="utf-8"
        )
        for op in ops:
            cls = _pascal(op.operation_id.replace(".", "_"))
            facade_name = _facade_name(op, taken)
            root_name = op_root[op.operation_id]
            facade_methods.append(render_facade_method(op, cls, root_name, facade_name))
            sync_facade_methods.append(render_sync_facade_method(op, root_name, facade_name))
            facade_imports.append(f"from pylzt.methods.{api}_{domain} import {cls}")
        print(f"staged methods/{api}_{domain}.py ({len(ops)} methods)")

    facade_body = "\n\n".join(facade_methods)
    _gen_path("facades", f"{api}.py").write_text(
        _render_facade_module(
            api, facade_body, facade_imports, model_names, reused, new_enum_names
        ),
        encoding="utf-8",
    )
    sync_facade_body = "\n\n".join(sync_facade_methods)
    _gen_path("facades", f"sync_{api}.py").write_text(
        _render_sync_facade_module(api, sync_facade_body, model_names, reused, new_enum_names),
        encoding="utf-8",
    )
    print(
        f"staged models/{api}.py ({len(models)}) + facades/{api}.py "
        f"({len(kept)} facades) + {len(new_enums)} new enums; "
        f"skipped {len(operations) - len(kept)} existing"
    )


def extract_enums(spec: dict[str, Any]) -> list[ExtractedEnum]:
    enums: list[ExtractedEnum] = []
    seen: set[str] = set()
    for schema_name, schema in spec.get("components", {}).get("schemas", {}).items():
        _collect_enums(schema_name, schema, enums, seen)
    for param_name, param in spec.get("components", {}).get("parameters", {}).items():
        schema = param.get("schema", {})
        _collect_enums(_pascal(param_name), schema, enums, seen)
    return enums


def _collect_enums(
    name: str, schema: dict[str, Any], enums: list[ExtractedEnum], seen: set[str]
) -> None:
    values = schema.get("enum")
    if values and all(isinstance(v, str) for v in values) and name not in seen:
        seen.add(name)
        members = tuple((_enum_member_id(v), v) for v in values)
        enums.append(ExtractedEnum(name=name, members=members))
    for prop_name, prop_schema in schema.get("properties", {}).items():
        if isinstance(prop_schema, dict):
            _collect_enums(_pascal(prop_name), prop_schema, enums, seen)


def render_enum(e: ExtractedEnum) -> str:
    member_lines = "\n".join(f"    {member} = {json.dumps(value)}" for member, value in e.members)
    return f'''class {e.name}(StrEnum):
    """Generated by forge — DO NOT EDIT. From the official OpenAPI spec."""

{member_lines}
'''


def generate_enums(spec: dict[str, Any], api: str) -> None:
    enums = extract_enums(spec)
    out_file = GENERATED_DIR / f"{api}_openapi_enums.py"
    blocks = [
        f'"""Generated by forge — DO NOT EDIT. Draft enums for {api}, extracted by',
        "`dev.codegen.generator --enums` from the official readme.io OpenAPI spec — review",
        'each before moving it into src/pylzt/types.py."""',
        "",
        "from __future__ import annotations",
        "",
        "from enum import StrEnum",
        "",
    ]
    blocks.extend(render_enum(e) for e in enums)
    out_file.write_text("\n\n".join(blocks), encoding="utf-8")
    print(f"wrote {out_file} ({len(enums)} enums)")


def _param_sig(p: ExtractedParam) -> str:
    return f"{p.name}:{p.annotation}" if p.required else f"{p.name}:{p.annotation}?"


def _md_cell(text: str) -> str:
    return text.replace("|", "\\|").replace("\n", " ")


def generate_docs(spec: dict[str, Any]) -> str:
    """Render a compressed Markdown reference: per-domain method tables + typed models + enums."""
    operations = extract_operations(spec)
    op_root, _op_unwrap, models, enums = _build_api_models(spec, operations)
    info = spec.get("info", {})
    title = info.get("title", "lzt API")
    version = info.get("version", "?")

    by_domain: dict[str, list[ExtractedOperation]] = {}
    for op in operations:
        by_domain.setdefault(op.domain, []).append(op)

    lines = [
        f"# {title} — generated reference",
        "",
        f"Spec version `{version}`. Auto-rendered by `dev.codegen.generator --docs` — "
        f"{len(operations)} methods, {len(models)} models, {len(enums)} enums "
        f"across {len(by_domain)} domains.",
        "",
    ]

    for domain, ops in sorted(by_domain.items()):
        ops = sorted(ops, key=lambda o: o.operation_id)
        lines.append(f"## {domain}")
        lines.append("")
        lines.append("| Op | HTTP | Path | Params | Returns |")
        lines.append("|----|------|------|--------|---------|")
        for op in ops:
            params = ", ".join(_param_sig(p) for p in _sorted_params(op)) or "—"
            returns = op_root.get(op.operation_id) or "—"
            oid = op.operation_id
            op_cell = f"[`{oid}`]({op.source_url})" if op.source_url else f"`{oid}`"
            lines.append(
                f"| {op_cell} | {op.http_method} | `{op.path}` | {_md_cell(params)} | {returns} |"
            )
        lines.append("")

    if models:
        lines.append("## Models")
        lines.append("")
        for m in models:
            fields = ", ".join(f"{f.name}:{f.annotation}" for f in m.fields)
            lines.append(f"- `{m.name}` — {_md_cell(fields)}")
        lines.append("")

    if enums:
        lines.append("## Enums")
        lines.append("")
        for e in enums:
            members = ", ".join(v for _, v in e.members)
            lines.append(f"- `{e.name}` — {_md_cell(members)}")
        lines.append("")

    return "\n".join(lines)


def generate_docs_file(spec: dict[str, Any], api: str) -> None:
    out_file = GENERATED_DIR / "docs" / f"{api}.md"
    out_file.parent.mkdir(parents=True, exist_ok=True)
    out_file.write_text(generate_docs(spec), encoding="utf-8")
    n = len(extract_operations(spec))
    print(f"wrote {out_file} ({n} methods documented)")


